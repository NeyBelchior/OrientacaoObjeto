package oo.claseAbstrata;

public class Teoria {
	
	/*
	 * Métodos Abstratos -----> métodos que não tem uma implementação  
	 *Classe concreta:
	 * 
	 * É uma classe no qual, todos os metodos estão definidos ou seja 100% dos métodos estão implementados (possuem corpos)
	 * {}
	 * 
	 * 
	 * Abstrata : É classe que pode ter todos os métodos abstratos , como ela pode ter parte dos metodos abstratos e como ela poder completa
	 * no sentido de ter todos os métodos definidos. 
	 * NÃO PODE SER INSTANCIADA 
	 * 
	 * Por que criar uma classe abstrata?
	 * Quando você tem herença, quando  você quer criar uma classe super que vai ser usada para ser herdado por outras classes concretas
	 * NÃO FAZ SENTIDO você criar uma classe abstrata que NUNCA SERA USADA. a não ser que você crie uma classe abstrata
	 * com todos os metodos statics para serem usados, mas via de regra usar sempre quando for utilizar herança
	 * 
	 * Animal >conceito abstrato> cachorro concreto
	 * Cachorro Abstrato > lessi concreto 
	 * 
	 * 
	 * Interface: 
	 * Em uma interface nós temos os métodos mas esses métodos não tem corpo( tem apenas a definição do método, qual o tipo de retorno qual o nome do metodo
	 * e quais são os parametros que precisam ser passados).
	 * todas classe que implementa uma interface, precisa definir esses métodos
	 * 
	 * 
	 * 
	 * 
	 * 
	 */

}
